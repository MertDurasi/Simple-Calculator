package util;

public class Calc {

	public static double equation(double result, double operand) {
		return operand;
	}
	
	public static double addition(double summand1, double summand2) {
		return summand1 + summand2;
	}
	
	public static double subtraction(double minuend, double subtrahend) {
		return minuend - subtrahend;
	}
	
	public static double multiplication(double factor1, double factor2) {
		return factor1 * factor2;
	}
	
	public static double division(double dividend, double divisor) {
		return dividend / divisor;
	}
	
	public static double modulus(double dividend, double divisor) {
		return dividend % divisor;
	}
	
}
